#3300 - Да се напише shell скрипт, който чете от стандартния вход имената на 3 файла, обединява редовете на първите два (man paste), 
#подрежда ги по азбучен ред и резултата записва в третия файл.

#!/bin/bash

read -p "Give me three files:" file1 file2 file3
paste ${file1} ${file2} | sort -r > ${file3}
